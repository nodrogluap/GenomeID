_PENG_BITS = (26, 45, 64, 48, 8, 21, 46, 30, 36, 10, 51, 14, 38, 60, 52,
              53, 42, 16, 33, 61, 40, 56, 18)

_ALLOSOME_FREQ: Dict[int, Tuple[float, float]] = {
    73:  (1-0.418, 0.418),   75:  (1-0.489, 0.489),  77:  (0.495,  1-0.495),
    79:  (1-0.341, 0.341),   81:  (1-0.6,   0.4),     83:  (0.485,  1-0.485),
    85:  (1-0.489, 0.489),   87:  (1-0.484, 0.484),  89:  (1-0.426, 0.426),
    91:  (0.388,   1-0.338), 93:  (1-0.408, 0.408),  95:  (1-0.453, 0.453),
    97:  (1-0.427, 0.427),   99:  (0.415,   1-0.415), 101: (0.441,  1-0.441),
    103: (1-0.332, 0.332),   105: (0.368,   1-0.368), 107: (1-0.447, 0.447),
    109: (1-0.412, 0.412),   111: (1-0.409, 0.409),  113: (0.273,  1-0.273),
    115: (1-0.426, 0.426),   117: (1-0.336, 0.336),  119: (1-0.336, 0.336),
}

_PROB_SUCCESS = 0.6


def _bin2dec(bits: str) -> int:
    """Perl: unpack("N", pack("B32", substr("0" x 32 . $bits, -32)))."""
    return int(bits.zfill(32)[-32:], 2)


def _check_version(bin_ids: List[str]) -> None:
    v1 = bin_ids[0][66:72]
    v2 = bin_ids[0][66:72]   # NOTE: Perl bug - both use bin_ids[0]
    if v1 != v2:
        raise RuntimeError("Genome IDs are incompatible versions")


def _cAutosome_match(bin_ids: List[str], bits: List[int],
                     skip: int, more_miss: int) -> int:
    matches = 0
    for bit in bits:
        bit_idx = bit - 1
        if skip != 0 and not int(bin_ids[more_miss][bit_idx]):
            skip -= 1
            continue
        matches += int(bin_ids[0][bit_idx] == bin_ids[1][bit_idx])
    return matches


def autosome_prob(id1: str, id2: str) -> Tuple[float, int]:
    """Port of Perl autosome_prob() from GenomeID.pm / pvalue.pl."""
    try:
        from scipy.stats import binom  # type: ignore[import]
    except ImportError:
        raise ImportError("scipy is required for autosome_prob")

    bin_ids = [_unpack_b64_to_bits(id1), _unpack_b64_to_bits(id2)]
    _check_version(bin_ids)

    prob = 0.0
    good_data = 1

    f_bits = [_bin2dec(bin_ids[0][:6]), _bin2dec(bin_ids[1][:6])]
    more_missing = 1 if f_bits[1] > f_bits[0] else 0
    fb = f_bits[more_missing]

    skip = 0
    if fb == 0:
        bits = list(range(7, 67))
    elif fb == 59:
        bits = list(_PENG_BITS) + [65, 66]
    elif fb == 63:
        bits = list(range(7, 67))
        good_data = 0
    elif 60 <= fb <= 62:
        bits = list(range(7, 67))
        skip = fb - 58
    else:
        bits = [b for b in range(7, 67) if b != fb + 6]

    k = _cAutosome_match(bin_ids, bits, skip, more_missing)
    n = len(bits) - skip

    if skip % 2:
        pass  # FIX THIS (matches Perl TODO)
    elif len(bits) == 58:
        n -= 1
    else:
        k -= skip // 2
        # Perl: 1 - pbinom(k, n, prob_success)
        # scipy pbinom = CDF; Perl's pbinom is CDF of Binomial
        prob = 1.0 - float(binom.cdf(k, n, _PROB_SUCCESS))

    return (prob, good_data)


def allosome_prob(id1: str, id2: str) -> float:
    """Port of Perl allosome_prob() from GenomeID.pm."""
    if len(id1) != 20 or len(id2) != 20:
        raise ValueError("Id for allosome probability must be 20 characters")

    bin_ids = [_unpack_b64_to_bits(id1), _unpack_b64_to_bits(id2)]
    _check_version(bin_ids)

    prob = 1.0
    male_present = (bin_ids[0][65:66] == "1" or bin_ids[1][65:66] == "1")

    for i in range(72, 119, 2):
        if male_present:
            marker1 = bin_ids[0][i+1:i+2]
            marker2 = bin_ids[1][i+1:i+2]
        else:
            marker1 = bin_ids[0][i:i+2]
            marker2 = bin_ids[1][i:i+2]

        if marker1 != marker2:
            continue
        if marker1 == "10":
            continue

        bit = i + 1
        freq = _ALLOSOME_FREQ.get(bit)
        if freq is None:
            continue

        power = 1 if male_present else 2

        if marker1 == "01":
            prob *= (2.0 * (freq[0] * freq[1])) ** 2
        else:
            j = int(marker1[0])
            prob *= freq[j] ** power

    return prob


def mayRelated(id1: str, id2: str) -> Any:
    """Port of Perl mayRelated() from GenomeID.pm."""
    if len(id1) != 20 or len(id2) != 20:
        raise ValueError("Id for allosome probability must be 20 characters")

    try:
        from scipy.stats import binom  # type: ignore[import]
    except ImportError:
        raise ImportError("scipy is required for mayRelated")

    bin_ids = [_unpack_b64_to_bits(id1), _unpack_b64_to_bits(id2)]
    _check_version(bin_ids)

    prob = 1.0
    male1 = bin_ids[0][64:65]
    male2 = bin_ids[1][64:65]
    skips = 0

    for i in range(72, 119, 2):
        marker1 = bin_ids[0][i+1:i+2] if male1 == "1" else bin_ids[0][i:i+2]
        marker2 = bin_ids[1][i+1:i+2] if male2 == "1" else bin_ids[1][i:i+2]

        if marker1 == "10" and male1 != "1":
            continue
        if marker2 == "10" and male2 != "1":
            continue

        if not _mandelian(male1, male2, marker1, marker2):
            skips += 1
        if skips > 2:
            return -1

        freq = _ALLOSOME_FREQ.get(i + 1)
        if freq is None:
            continue
        q, p = freq
        pr = 1.0

        if male1 == "1" and male2 != "1":   # father → daughter
            if marker1 == "0":
                p = q
            pr /= p
        if male1 != "1" and male2 == "1":   # mother → son
            if marker2 == "0":
                p = q
                pr *= p if marker1 == "00" else (1 - p)
            else:
                pr *= (1 - p) if marker1 == "11" else p
        if male1 != "1" and male2 != "1":   # mother → daughter
            all_bits = list(range(73, 121))
            k = _cAutosome_match(bin_ids, all_bits, 0, 0)
            n = len(all_bits)
            p_val = 1.0 - float(binom.cdf(k, n, _PROB_SUCCESS))
            return 1.0 / p_val

        prob *= pr

    return prob


def _mandelian(male1: str, male2: str, mark1: str, mark2: str) -> bool:
    """Port of Perl mandelian()."""
    if male1 == "1" and male2 == "1":
        return mark1 == mark2
    if male1 == "1":
        return mark1 in mark2
    if male2 == "1":
        return mark2 in mark1
    # female + female
    if mark2 and (mark1[0:1] in mark2 or (len(mark1) > 1 and mark1[1:2] in mark2)):
        return True
    return False


