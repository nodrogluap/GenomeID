# TBI parser - mirrors Perl tbi()
# ---------------------------------------------------------------------------
def _parse_tbi(st: _State) -> None:
    """Parse a bgzipped + tabix-indexed VCF using pysam.TabixFile."""
    if pysam is None:
        raise ImportError("pysam is required for tbi parsing")

    missing_markers = 0
    only_peng = True
    bit_mis = 0

    st.prefix = "chr"
    tbx = pysam.TabixFile(st.file)

    def _fetch_row(tbx_obj: Any, chr_name: str, pos: str) -> Optional[str]:
        """Try fetching with and without 'chr' prefix; return matching row."""
        pos_i = int(pos)
        for query_chr in (chr_name, f"{st.prefix}{chr_name}"):
            try:
                rows = list(tbx_obj.fetch(query_chr, pos_i - 1, pos_i))
            except (ValueError, KeyError):
                continue
            if not rows:
                continue
            if len(rows) == 1:
                return rows[0]
            # Multiple rows: find the one where col[1] == pos
            for row in rows:
                cols = row.split("\t")
                if len(cols) > 1 and cols[1] == pos:
                    return row
            return rows[-1]  # fallback: last row
        return None

    # --- Autosomal markers ---
    for key in st.autoSomes:
        chr_, pos = key.split(":", 1)
        is_peng = st.pengelly.get(key, 0)
        bit = int(st.bit_loc[key]) - 1

        row = _fetch_row(tbx, chr_, pos)

        if row is None:
            if st.ref:
                st.AMB[bit] = "0"
                continue
            st.AMB[bit] = "0"
            missing_markers += 1
            bit_mis = bit
            if is_peng:
                only_peng = False
            continue

        col = row.split("\t")

        # guessHG using REF column (col[3])
        if st.guess_hg:
            ref_field = col[3] if len(col) > 3 else ""
            cont = _guessHG(st, key=key, smp_ref=ref_field)
            if not cont:
                tbx.close()
                _parse_tbi(st)
                return

        sample = col[st.readCol] if st.readCol < len(col) else ""
        mis = bool(_MISR.match(sample))
        zyg = 0 if (_HOMO1.match(sample) or _HOMO2.match(sample)) else 1

        if mis:
            if st.ref:
                st.AMB[bit] = "0"
                continue
            st.AMB[bit] = "0"
            missing_markers += 1
            bit_mis = bit
            if is_peng:
                only_peng = False
        else:
            st.AMB[bit] = str(zyg)
            if not is_peng:
                only_peng = False

    _genMADIB(st, miss_count=missing_markers, only_peng=only_peng, bit_mis=bit_mis)

    if not st.sex:
        tbx.close()
        return

    # --- Allosomal markers ---
    for key in st.allosomes:
        chr_, pos = key.split(":", 1)

        row = _fetch_row(tbx, chr_, pos)

        if row is None:
            if chr_ == "Y":
                continue
            if key not in st.bit_loc:
                continue
            bit = int(st.bit_loc[key]) - 1
            if st.ref:
                ref_al = st.ref_allel.get(key, "")
                anc = st.allosomes[key].split(":", 1)[0]
                if ref_al == anc and st.SMB[0] != "1":
                    st.XMB[bit] = "0"
                else:
                    st.XMB[bit + 1] = "1"
            continue

        _xmb_builder(st, key, row)

    tbx.close()


