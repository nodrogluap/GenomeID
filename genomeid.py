# BAM parser - mirrors Perl bam() + bam_zygosity()
# ---------------------------------------------------------------------------
def _bam_zygosity(st: _State, bam_obj: Any, chr_name: str, loc: str,
                  al1: Optional[str] = None, al2: Optional[str] = None,
                  smb: bool = False, anc: Optional[str] = None,
                  alt: Optional[str] = None) -> Any:
    """Port of Perl bam_zygosity().

    Returns:
      -1 or "1:0"  → no reads
      int 0 or 1   → autosome zygosity (0=homo, 1=hetero)
      str "X:Y"    → sex-chr zygosity (bit1:bit2)
      int 1 or 0   → Y-chr presence
    """
    pos = int(loc)
    alleles: Dict[str, int] = {"G": 0, "C": 0, "T": 0, "A": 0}
    reads = 0

    for pileup_col in bam_obj.pileup(chr_name, pos - 1, pos,
                                     truncate=True, stepper="nofilter"):
        if pileup_col.reference_pos != pos - 1:
            continue
        for pread in pileup_col.pileups:
            if pread.is_del or pread.is_refskip:
                continue
            aln = pread.alignment

            # Mapping quality filter (Perl: $f->qual >= $maq)
            if aln.mapping_quality < st.maq:
                continue

            qpos = pread.query_position
            if qpos is None:
                continue

            # Base quality filter (Perl: ($f->qscore)[$start] >= $baq)
            bq = aln.query_qualities[qpos] if aln.query_qualities is not None else 0
            if bq < st.baq:
                continue

            bp = aln.query_sequence[qpos] if aln.query_sequence else ""
            if bp in alleles:
                alleles[bp] += 1

            reads += 1

    # Sort alleles by count descending
    sorted_alleles = sorted(alleles, key=lambda k: alleles[k], reverse=True)
    al_1 = sorted_alleles[0]
    al_2 = sorted_alleles[1]
    al1_cnt = alleles[al_1]
    al2_cnt = alleles[al_2]

    # No reads
    if al1_cnt == 0 and al2_cnt == 0:
        return "1:0" if smb else -1

    # Noise / depth check
    zyg_prob = al1_cnt / (al1_cnt + al2_cnt) * 100
    if (zyg_prob ** reads) < st.noise or reads < st.minRead_depth:
        return "1:0" if smb else 0

    if smb:
        if chr_name.endswith("Y") or chr_name == "Y":
            if al_1 == anc and al1_cnt > 0.9 * (al1_cnt + al2_cnt):
                return 1
            return 0

        # X chromosome (diploid / het)
        if al1_cnt >= 0.90 * (al1_cnt + al2_cnt):
            if al_1 == anc:
                return "0:0"
            if al_1 == alt:
                return "1:1"
            return "1:0"
        else:
            if alleles.get(anc, 0) == 0 or alleles.get(alt, 0) == 0:
                return "1:0"
            return "0:1"

    return 0 if al1_cnt >= 0.90 * (al1_cnt + al2_cnt) else 1


def _parse_bam(st: _State) -> None:
    """Port of Perl bam() using pysam."""
    if pysam is None:
        raise ImportError("pysam is required for BAM parsing")

    bam_obj = pysam.AlignmentFile(st.file, "rb")

    # Detect chr prefix
    st.prefix = ""
    for rname in bam_obj.references:
        m = re.match(r"^([cC][hH][rR]+)\d+", rname)
        if m:
            st.prefix = m.group(1)
            break

    # Guess hg from chr1 length
    if st.guess_hg:
        chr1 = f"{st.prefix}1"
        try:
            length = bam_obj.get_reference_length(chr1)
        except (ValueError, KeyError):
            length = 0
        if length not in _CHR1_LENGTHS:
            bam_obj.close()
            raise RuntimeError("Could not determine human genome version, please specify")
        _select_hg(st, _CHR1_LENGTHS[length])
        st.guess_hg = False

    missing_markers = 0
    only_peng = True
    bit_mis = 0

    # Autosomal markers
    for key, autosome_val in st.autoSomes.items():
        chr_, pos = key.split(":", 1)
        fir, las = autosome_val.split(":", 1)
        is_peng = st.pengelly.get(key, 0)
        bit = int(st.bit_loc[key]) - 1

        zyg = _bam_zygosity(st, bam_obj, f"{st.prefix}{chr_}", pos,
                             al1=fir, al2=las)

        if zyg == -1:
            st.AMB[bit] = "0"
            missing_markers += 1
            bit_mis = bit
            if is_peng:
                only_peng = False
        else:
            st.AMB[bit] = str(zyg)
            if not is_peng:
                only_peng = False

    _genMADIB(st, miss_count=missing_markers, only_peng=only_peng, bit_mis=bit_mis)

    if not st.sex:
        bam_obj.close()
        return

    # Allosomal markers
    for key, allo_val in st.allosomes.items():
        chr_, pos = key.split(":", 1)
        anc, alt_al = allo_val.split(":", 1)

        zyg = _bam_zygosity(st, bam_obj, f"{st.prefix}{chr_}", pos,
                             smb=True, anc=anc, alt=alt_al)

        if chr_ == "X":
            bit = int(st.bit_loc[key]) - 1
            b1, b2 = str(zyg).split(":", 1) if ":" in str(zyg) else ("1", "0")
            st.XMB[bit]     = b1
            st.XMB[bit + 1] = b2
        else:  # Y
            if zyg != 1:
                continue
            parts = str(st.bit_loc[key]).split(":", 2)
            y_bit   = parts[0] if len(parts) > 0 else "0"
            y_depth = int(parts[1]) if len(parts) > 1 else 0
            mark_id = parts[2] if len(parts) > 2 else ""
            st.SMB[0] = "1"

            if st.Y_marker_name != 1:
                tree = mark_id.split("-", 1)[0] if mark_id else ""
                if (st.Y_marker_name == "" or
                        (isinstance(st.Y_marker_name, str) and
                         tree in st.Y_marker_name)):
                    st.Y_marker_name = tree
                else:
                    st.Y_marker_name = 1

            if y_depth <= st.max_depth:
                continue
            st.max_depth = y_depth
            st.Y_marker_id = y_bit

    bam_obj.close()


