def generate_id(**input_: Any) -> str:
    """Generate a GenomeID string from a BAM, VCF, or tabix-indexed VCF file.

    Parameters mirror the Perl module's generate_id() hash arguments.
    Returns a base64-encoded string (12 chars without sex, 20 chars with sex).

    Example::

        gid = generate_id(file='sample.vcf.gz', type='tbi', hg='hg38', sex=True)
    """
    st = _State()
    _regen(st)

    # Determine human genome build
    if "hg" in input_ and input_["hg"] is not None:
        _select_hg(st, input_["hg"])
    else:
        st.guess_hg = True
        _select_hg(st, "hg38")   # Perl starts guess with hg38

    # Store input flags
    st.baq           = int(input_.get("baq", 30))
    st.maq           = int(input_.get("maq", 20))
    st.noise         = float(input_.get("noise", 0.05))
    st.sex           = bool(input_.get("sex", False))
    st.file          = input_.get("file")
    st.ucn           = int(input_.get("ucn", 0))
    st.ref           = int(input_.get("ref", 0))
    st.sampleName    = str(input_.get("sampleName") or "")
    st.minRead_depth = int(input_.get("minDepth", 10))

    file_type = input_.get("type")
    if file_type not in {"bam", "vcf", "tbi"}:
        raise ValueError("No supported file type was specified")
    st.type = file_type

    # Determine sample column if sampleName given (non-BAM only)
    if st.sampleName and file_type != "bam":
        _detReadCol(st, file_type)

    # Initialise XMB: every even-indexed slot = "1" (= undefined marker "10")
    for i in range(0, len(st.XMB), 2):
        st.XMB[i] = "1"

    # Parse the file
    if file_type == "bam":
        _parse_bam(st)
    elif file_type == "vcf":
        _parse_vcf(st)
    else:
        _parse_tbi(st)

    # Generate version block (6 bits, VERSION=1 → "000001")
    st.GVB = format(VERSION, "06b")

    # Build Y-marker bits into XMB when Y present and sex flag set
    if st.SMB[0] == "1" and st.sex:
        # Perl: $Y_marker_id may be a string (e.g. "1") for BAM or an int
        y_id = int(st.Y_marker_id) if st.Y_marker_id else 0
        y_bits = format(y_id, "024b")

        for i in range(0, len(st.XMB), 2):
            st.XMB[i] = y_bits[i // 2]

        # Perl: if Y_marker_name eq 1 → XMB[0]=1, else XMB[0]=0
        st.XMB[0] = "1" if st.Y_marker_name == 1 else "0"

    # SMB[1] = 1 if ucn OR not male (no Y marker)
    st.SMB[1] = "1" if (st.ucn or st.SMB[0] != "1") else "0"

    xmb_str = "".join(st.XMB) if st.sex else ""
    amb_str = "".join(st.AMB)
    smb_str = "".join(st.SMB)

    gen_string = f"{st.MADIB}{amb_str}{smb_str}{st.GVB}{xmb_str}"
    return _pack_bits_to_b64(gen_string)
