# ---------------------------------------------------------------------------
# Bit-packing / base64 (mirrors Perl: encode_base64 pack 'B*', $bits)
# ---------------------------------------------------------------------------
def _pack_bits_to_b64(bit_string: str) -> str:
    """Encode a bit-string exactly as Perl's ``encode_base64(pack('B*', $s))``.

    ``pack 'B*'`` packs MSB-first (big-endian), padding on the right with zeros
    to a byte boundary.  ``encode_base64`` then base64-encodes the bytes.  The
    Perl caller strips all whitespace, so we return plain base64 with no newline.
    """
    pad = (-len(bit_string)) % 8
    bit_string += "0" * pad
    n_bytes = len(bit_string) // 8
    if n_bytes == 0:
        b = b""
    else:
        b = int(bit_string, 2).to_bytes(n_bytes, byteorder="big")
    return base64.b64encode(b).decode("ascii")


def _unpack_b64_to_bits(gid: str) -> str:
    raw = base64.b64decode(gid)
    return "".join(f"{byte:08b}" for byte in raw)


# ---------------------------------------------------------------------------
# genMADIB - mirrors Perl genMADIB()
# ---------------------------------------------------------------------------
def _genMADIB(st: _State, miss_count: int, only_peng: bool, bit_mis: int) -> None:
    if miss_count == 0:
        st.MADIB = "000000"
    elif miss_count > 4:
        # Perl: sprintf("%b", 63) = "111111"  (no left-padding applied)
        st.MADIB = format(63, "b")   # "111111"
    elif only_peng:
        # Perl: sprintf("%b", 59) = "111011"  (no padding)
        st.MADIB = format(59, "b")   # "111011"
    elif miss_count == 1:
        # Perl: "0" x (6 - length) . sprintf("%b", bMis)
        st.MADIB = format(bit_mis, "06b")
    else:
        st.MADIB = format(miss_count, "06b")


# ---------------------------------------------------------------------------
# guessHG - mirrors Perl guessHG()
# ---------------------------------------------------------------------------
def _guessHG(st: _State, key: str, smp_ref: str) -> bool:
    """Guess / confirm human genome build.

    Returns True  → continue parsing with current build.
    Returns False → switched build; caller should restart parsing.
    """
    cur_ref = st.ref_allel.get(key, "")

    for hg, ref_map in REF_ALLEL_STATIC.items():
        if hg == st.ref_hg:
            continue
        if key not in ref_map:
            continue
        ref_allel_val = ref_map[key]

        if ref_allel_val == smp_ref:
            # If initial guess was also right (same ref allele in both), keep
            if ref_allel_val == cur_ref:
                return True

            # Switch to the other build and signal restart
            st.guess_hg = False
            _select_hg(st, hg)
            return False

    # Key not in current build's bit_loc → not an informative locus; skip
    if key not in st.bit_loc:
        return True

    # Current build matches
    if cur_ref == smp_ref:
        st.guess_hg = False
        return True

    raise RuntimeError("Reference genome version not determined, please specify")


# ---------------------------------------------------------------------------
# detReadCol - mirrors Perl detReadCol()
# ---------------------------------------------------------------------------
def _detReadCol(st: _State, file_type: str) -> None:
    """Determine which column to read for sample genotype."""
    if not st.sampleName:
        return

    if file_type == "tbi":
        if pysam is None:
            raise ImportError("pysam is required for tbi detReadCol")
        tbx = pysam.TabixFile(st.file)
        header_lines = list(tbx.header)
        tbx.close()
        if not header_lines:
            return
        last_line = header_lines[-1]
    else:  # vcf
        last_line = ""
        with open(st.file, "rt", encoding="utf-8", errors="replace") as fh:
            for line in fh:
                if not line.startswith("#"):
                    break
                last_line = line.rstrip("\n")

    if not last_line:
        return

    header = last_line.split("\t")
    # Perl: first_index{ /$sampleName/ } @header   (regex match)
    read_col = -1
    pattern = re.compile(st.sampleName)
    for i, col in enumerate(header):
        if pattern.search(col):
            read_col = i
            break
    if read_col < 2:
        read_col = 9
    st.readCol = read_col


# ---------------------------------------------------------------------------
# xmb_builder - mirrors Perl xmb_builder()
# ---------------------------------------------------------------------------
def _xmb_builder(st: _State, key: str, row: str) -> None:
    """Build XMB bits for one allosomal VCF/tabix row.

    NOTE: The Perl code has a known bug for Y chromosome markers in VCF/TBI
    mode: it splits ``allosomes{key}`` (value like 'C:T') instead of
    ``bit_loc{key}`` (value like '1:4:A0-L994').  The resulting 'depth' field
    is a nucleotide letter ('T', 'C' etc.) which evaluates to 0 numerically in
    Perl, so the ``depth > max_depth`` guard is always false (0 > 0 = false)
    and Y markers are never recorded via this path.  We replicate that behavior
    for byte-for-byte compatibility.
    """
    col = row.rstrip("\n").split("\t")
    chr_pos = key.split(":", 1)
    chr_ = chr_pos[0]

    anc_alt = st.allosomes[key].split(":", 1)
    anc = anc_alt[0]
    alt = anc_alt[1] if len(anc_alt) > 1 else ""

    if chr_ == "Y":
        # Check for missing genotype
        sample_field = col[st.readCol] if st.readCol < len(col) else ""
        if _MISR.match(sample_field):
            return

        # --- PERL BUG REPLICATION ---
        # Perl uses allosomes{key} (= 'C:T') instead of bit_loc{key} (= 'bit:depth:marker_id').
        # So 'bit' = 'C', 'depth' = 'T', 'marker_id' = None.
        # 'T' as Perl number = 0; 0 > 0 (max_depth) is False → always return.
        allo_parts = st.allosomes[key].split(":")
        depth_str  = allo_parts[1] if len(allo_parts) > 1 else ""
        marker_id  = allo_parts[2] if len(allo_parts) > 2 else ""

        # Y_marker_name tracking (mirrors Perl, even if we always return below)
        if st.Y_marker_name != 1:
            tree = marker_id.split("-", 1)[0] if marker_id else ""
            if (st.Y_marker_name == "" or
                    (isinstance(st.Y_marker_name, str) and tree in st.Y_marker_name)):
                st.Y_marker_name = tree
            else:
                st.Y_marker_name = 1

        # Numeric conversion of depth_str (letter → 0 in Perl)
        try:
            depth = int(depth_str)
        except (ValueError, TypeError):
            depth = 0

        if not (depth > st.max_depth):
            return

        st.max_depth = depth
        st.Y_marker_id = allo_parts[0] if allo_parts else ""
        st.SMB[0] = "1"
        return

    # --- X chromosome ---
    bit = int(st.bit_loc[key]) - 1

    # Handle multiple alternate alleles (col[4])
    alt4 = col[4] if len(col) > 4 else ""
    if not re.match(r"^\s*\w{1}\s*$", alt4):
        st.ucn = 1
        if alt in alt4:
            col[4] = alt
        else:
            col[4] = "P"

    sample = col[st.readCol] if st.readCol < len(col) else ""

    # Males sometimes have single-char genotype (haploid X)
    if len(sample) == 1:
        st.SMB[0] = "1"
        if sample == "0":
            ref_col = col[3] if len(col) > 3 else ""
            st.XMB[bit + 1] = "0" if ref_col == anc else "1"
        elif sample == "1" and (col[4] if len(col) > 4 else "") == alt:
            st.XMB[bit + 1] = "1"
        return

    mis = bool(_MISR.match(sample))

    # Homozygous reference (0/0)
    if _HOMO2.match(sample):
        ref_col = col[3] if len(col) > 3 else ""
        is_alt = 0 if ref_col == anc else 1
        st.XMB[bit + is_alt] = "1" if is_alt else "0"
    # Homozygous alternate (1/1)
    elif _HOMO1.match(sample):
        alt_col = col[4] if len(col) > 4 else ""
        is_alt = 1 if alt_col == alt else 0
        st.XMB[bit + is_alt] = "1" if is_alt else "0"
    # Heterozygous
    elif not mis:
        st.XMB[bit] = "0"
        st.XMB[bit + 1] = "1"


