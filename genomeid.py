    found = 0
    found_peng = 0

    with open(st.file, "rt", encoding="utf-8", errors="replace") as fh:
        for line in fh:
            if line.startswith("#"):
                continue

            fields = line.rstrip("\n").split("\t")
            chr_  = fields[0]
            # strip chr prefix - Perl: $chr =~ s/^chr//; (lowercase only, case-sensitive)
            if chr_.startswith("chr"):
                chr_ = chr_[3:]
            loc = fields[1] if len(fields) > 1 else ""
            key = f"{chr_}:{loc}"

            if st.guess_hg:
                ref_field = fields[3] if len(fields) > 3 else ""
                cont = _guessHG(st, key=key, smp_ref=ref_field)
                if not cont:
                    _parse_vcf(st)
                    return

            if key not in st.bit_loc:
                continue

            bit = int(st.bit_loc[key]) - 1
            sample = fields[st.readCol] if st.readCol < len(fields) else ""
            mis = bool(_MISR.match(sample))

            # Sex-chromosome marker
            if key in st.allosomes and st.sex:
                st.ref_allel.pop(key, None)   # mirrors Perl: delete $ref_allel{$key} (silent)
                _xmb_builder(st, key, line)
                continue

            if key not in st.autoSomes:
                continue

            zyg = 0 if (_HOMO1.match(sample) or _HOMO2.match(sample)) else 1
            st.AMB[bit] = str(zyg * int(not mis))

            is_peng = st.pengelly.get(key, 0)
            found_peng += is_peng

            if mis:
                missing_markers += 1
                bit_mis = bit
                if is_peng:
                    only_peng = False
            elif not is_peng:
                only_peng = False

            found += int(not mis)

    # Reference-based fill-in for sex markers
    if st.ref and st.sex:
        for rkey, ref_al in list(st.ref_allel.items()):
            if rkey not in st.bit_loc or rkey not in st.allosomes:
                continue
            rbit = int(st.bit_loc[rkey]) - 1
            ranc = st.allosomes[rkey].split(":", 1)[0]
            if ref_al == ranc and st.SMB[0] != "1":
                st.XMB[rbit] = "0"
            else:
                st.XMB[rbit + 1] = "1"

    if not st.ref:
        missing_markers += 58 - found

    only_peng = bool(only_peng and found_peng == 24)
    _genMADIB(st, miss_count=missing_markers, only_peng=only_peng, bit_mis=bit_mis)


